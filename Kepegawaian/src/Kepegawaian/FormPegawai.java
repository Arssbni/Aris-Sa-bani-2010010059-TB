    private void tpegawaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tpegawaiMouseClicked
        // TODO add your handling code here:
        int i = tpegawai.getSelectedRow(); 
         
        if(i>-1){ 
            nip.setText(model.getValueAt(i, 0).toString()); 
            nama.setText(model.getValueAt(i, 1).toString());
            alamat.setText(model.getValueAt(i, 2).toString()); 
            jk.setSelectedItem(model.getValueAt(i, 3).toString()); 
            jabatan.setSelectedItem(model.getValueAt(i, 4).toString()); 
            tlp.setText(model.getValueAt(i, 5).toString());  
        }
    }//GEN-LAST:event_tpegawaiMouseClicked

