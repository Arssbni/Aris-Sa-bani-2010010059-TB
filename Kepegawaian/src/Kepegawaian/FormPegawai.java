    private void tampilkan() {
       int row = tpegawai.getRowCount();
       for(int a= 0; a<row;a++){
           model.removeRow(0);
       }
       try{
               Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/kepegawaian","root","");
                ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM pegawai");
                while(rs.next()){
                    String data []= {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)};
                    model.addRow(data);
                }
           } catch (SQLException ex) {
               Logger.getLogger(FormPegawai.class.getName()).log(Level.SEVERE, null, ex);
           }
      
       }
       
